# Assignment using equal operator.

var.1 = c(0,1,2,3)

# Assignment using leftward operator.

var.2 <- c("learn","R")

# Assignment using rightward operator.

c(TRUE,1)->var.3

print(var.1)
print(var.2)
print(var.3)

cat(" this is ",var.1 ,"\n")
cat(" this is ",var.2 ,"\n")
cat(" this is ",var.3 ,"\n")

